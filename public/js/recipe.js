$(document).ready(function () {
  makeMenu();

  $("#resetbutton").click(function () {
    if (String($(this).css("opacity")) == "1") {
      $(this).css("opacity", "0.3");
      makeNewMenu();
    }
  });
});

function makeNewMenu() {
  $("#menu").animate({ left: "100%" }, 200, function () {
    makeMenu();
    $("#menu").css("left", "-100%");
    $("#menu").animate({ left: "0%" }, 300, function () {
      $("#resetbutton").css({ opacity: "1" });
    });
  });
}

function makeMenu() {
  resetArrays();

  if (percentchance(50)) {
    barname = "The " + pullWordFromArray(streets);
  } else {
    barname =
      pullWordFromArray(streets) +
      "&nbsp;&amp;&nbsp;" +
      pullWordFromArray(streets);
  }

  for (var a = 0; a < 10; a++) {
    makeMenuItem();
  }
  makeMenuVisuals();
}

function makeMenuVisuals() {
  $("#menuitems").html("");
  for (var a = 0; a < menu.length; a++) {
    $("#menuitems").append(
      '<div class="oneitem">' +
        menu[a][0] +
        "&nbsp;&nbsp;&nbsp;&nbsp;" +
        "</div>"
    );
    if (a == 2 || a == 5) {
      $("#menuitems").append('<div class="menugap"></div>');
    }
  }
}

function makeMenuItem() {
  var menuitem = "";
  if (percentchance(20)) {
    // three-ingredient menu item
    if (percentchance(50)) {
      menuitem =
        makeOneFoodItem() +
        ", " +
        makeOneFoodItem() +
        " & " +
        makeOneFoodItem();
    } else {
      menuitem =
        makeOneFoodItem() +
        " with " +
        makeOneFoodItem() +
        " & " +
        makeOneFoodItem();
    }
  } else if (percentchance(40)) {
    // two-ingredient menu item
    if (percentchance(50)) {
      menuitem = makeOneFoodItem() + " & " + makeOneFoodItem();
    } else {
      menuitem = makeOneFoodItem() + " with " + makeOneFoodItem();
    }
  } else {
    // one-ingredient menu item
    menuitem = makeOneFoodItem();
  }
  menu.push([menuitem]);
}

function makeOneFoodItem() {
  var foodstring = "";
  if (percentchance(55)) {
    // add adjective to ingredient (ex: "salted")
    foodstring += pullWordFromArray(adjectives) + " ";
  }
  foodstring += pullWordFromArray(foods);
  if (percentchance(40)) {
    // add object to ingredient (ex: "bowl" / "salad")
    foodstring += " " + pullWordFromArray(foodshapes);
  }
  return foodstring;
}

function pullWordFromArray(thearray) {
  var randomindex = Math.floor(Math.random() * thearray.length);
  var wordtoreturn = thearray[randomindex];
  thearray.splice(randomindex, 1);
  return wordtoreturn;
}

function percentchance(percent) {
  var randomnumber = Math.floor(Math.random() * 101);
  if (percent > randomnumber) {
    return true;
  } else {
    return false;
  }
}

function resetArrays() {
  menu = [];

  foods = [];
  for (a = 0; a < allFoods.length; a++) {
    foods.push(allFoods[a]);
  }

  adjectives = [];
  for (b = 0; b < allAdjectives.length; b++) {
    adjectives.push(allAdjectives[b]);
  }

  foodshapes = [];
  for (c = 0; c < allFoodShapes.length; c++) {
    foodshapes.push(allFoodShapes[c]);
  }

  streets = [];
  for (a = 0; a < allStreets.length; a++) {
    streets.push(allStreets[a]);
  }
}
